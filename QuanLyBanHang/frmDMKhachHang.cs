        private void btnImportExcel_Click(object sender, EventArgs e)
        {
            using (OpenFileDialog openFileDialog = new OpenFileDialog())
            {
                openFileDialog.Filter = "Excel Files|*.xls;*.xlsx;*.xlsm";
                openFileDialog.Title = "Chọn file Excel để nhập khách hàng";

                if (openFileDialog.ShowDialog() == DialogResult.OK)
                {
                    string filePath = openFileDialog.FileName;
                    ImportCustomersFromExcel(filePath);
                }
            }
        }

        private void ImportCustomersFromExcel(string filePath)
        {
            using (var package = new ExcelPackage(new FileInfo(filePath)))
            {
                var worksheet = package.Workbook.Worksheets.First();
                int rowCount = worksheet.Dimension.Rows;
                int addedCount = 0; // Biến đếm số lượng bản ghi đã thêm

                for (int row = 2; row <= rowCount; row++) // Bắt đầu từ hàng thứ 2 (hàng đầu tiên là tiêu đề)
                {
                    string tenKhach = worksheet.Cells[row, 1].Text; // Lấy tên khách từ cột 1
                    string diaChi = worksheet.Cells[row, 2].Text; // Lấy địa chỉ từ cột 2
                    string dienThoai = worksheet.Cells[row, 3].Text; // Lấy điện thoại từ cột 3

                    // Kiểm tra dữ liệu không trống
                    if (string.IsNullOrWhiteSpace(tenKhach) || string.IsNullOrWhiteSpace(diaChi) || string.IsNullOrWhiteSpace(dienThoai))
                    {
                        MessageBox.Show($"Dữ liệu không hợp lệ ở hàng {row}. Bỏ qua hàng này.", "Thông báo", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                        continue; // Bỏ qua hàng này nếu dữ liệu không hợp lệ
                    }

                    // Tạo mã khách tự động
                    string maKhach;
                    do
                    {
                        maKhach = Functions.CreateKey("KH"); // Tạo mã khách mới
                    } while (Functions.CheckKey($"SELECT MaKhach FROM tblKhach WHERE MaKhach=N'{maKhach}'")); // Kiểm tra xem mã đã tồn tại chưa

                    // Chèn vào cơ sở dữ liệu
                    string sql = $"INSERT INTO tblKhach (MaKhach, TenKhach, DiaChi, DienThoai) VALUES (N'{maKhach}', N'{tenKhach}', N'{diaChi}', '{dienThoai}')";
                    Functions.RunSQL(sql);
                    addedCount++; // Tăng biến đếm
                }

                // Thông báo số lượng bản ghi đã thêm
                MessageBox.Show($"Đã thêm {addedCount} bản ghi vào cơ sở dữ liệu.", "Thông báo", MessageBoxButtons.OK, MessageBoxIcon.Information);
            }

            LoadDataGridView(); // Làm mới DataGridView để hiển thị dữ liệu mới
        }
